SUBROUTINE run_cw_solver() BIND(C, NAME='run_cw_solver_')

  USE cw_define_DDEs
  USE DDE_SOLVER_M
  USE ISO_C_BINDING

  IMPLICIT NONE

  INTEGER, DIMENSION(2) :: NVAR = (/NEQN, NLAGS/)

  DOUBLE PRECISION, DIMENSION(NLAGS) :: DELAYS  = (/ 1.5D0 /)
  DOUBLE PRECISION, DIMENSION(NEQN)  :: HISTORY = (/ 0.4D0, 0.4D0 /)

  TYPE(DDE_SOL)  :: SOL
  TYPE(DDE_OPTS) :: OPTS

  tau = 1.5D0

  SOL = DDE_SOLVER(NVAR,DDES,DELAYS,HISTORY,TSPAN=(/ 0.0D0, 200.0D0 /))

  CALL RELEASE_ARRAYS(SOL,OPTS)

END SUBROUTINE run_cw_solver