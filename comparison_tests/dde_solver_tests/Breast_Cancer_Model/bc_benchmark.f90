SUBROUTINE run_bc_solver() BIND(C, NAME='run_bc_solver_')

  USE bc_define_DDEs
  USE DDE_SOLVER_M
  USE ISO_C_BINDING

  IMPLICIT NONE

  INTEGER, DIMENSION(2) :: NVAR = (/NEQN, NLAGS/)

  DOUBLE PRECISION, DIMENSION(NLAGS) :: DELAYS = (/ 1.0D0 /)
  DOUBLE PRECISION, DIMENSION(NEQN)  :: HISTORY = (/ 1.0D0, 1.0D0, 1.0D0 /)

  TYPE(DDE_SOL)  :: SOL
  TYPE(DDE_OPTS) :: OPTS

  tau = 1.0D0

  OPTS = DDE_SET(RE=1.0D-9, AE=1.0D-9, MAX_STEPS = 100000 ) ! tolerances 1.0D-12 was too tight
  SOL = DDE_SOLVER(NVAR,DDES,DELAYS,HISTORY,TSPAN=(/ 0.0D0, 10000.0D0 /),OPTIONS=OPTS)

  CALL RELEASE_ARRAYS(SOL,OPTS)

END SUBROUTINE run_bc_solver